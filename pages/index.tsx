import { useState } from 'react'
import { random } from 'lodash'
import type { MouseEventHandler } from 'react'
import Head from 'next/head' 
import { LazyImage } from '@/components/RandomFox'

interface ImageItem {
  id: string,
  url: string
}

const randomNumber = () => random(1, 100)
const generateId = (): string => Math.random().toString(36).substr(2, 9)

export default function Home() {
  const [images, setImages] = useState<ImageItem[]>([])

  const addNewFox: MouseEventHandler = (event) => {
    setImages([
      ...images,
      {id: generateId(), url: `https://randomfox.ca/images/${randomNumber()}.jpg`}
    ])
  }

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className='text-center mx-auto w-64'>
        <h1 className="text-3xl font-bold underline">
          Random Fox!
        </h1>

          <button 
            onClick={addNewFox}
            className='bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 border border-blue-700 rounded my-2'
          >Add Image</button>

          {images.map((image) => (
            <div key={image.id} className='p-4'>
              <LazyImage 
                src={image.url}
                // onLazyLoad={(img) => {console.log(img.src)}}  
              />
            </div>
          ))}
      </main>
    </>
  )
}
